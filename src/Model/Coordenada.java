package Model;

public class Coordenada {

	private int valorX;
	private int valorY;
	
	public Coordenada(int valorX, int valorY) {
		this.valorX = valorX;
		this.valorY = valorY;
	}

	public int getValorX() {
		return valorX;
	}

	public void setValorX(int valorX) {
		this.valorX = valorX;
	}

	public int getValorY() {
		return valorY;
	}

	public void setValorY(int valorY) {
		this.valorY = valorY;
	}
	
}
